public class SolucionEjercicios {
    public double areaTriangulo(double base, double height){
        return (base*height)/2;
    }
    public double SumaNum(double Num1, double Num2){
        return Num1+Num2;
    }
    public double NumCuadrado(double Num1){

        return Num1*Num1;
    }
    public double EurosaDolar(double Num1){
        return Num1*1.08;
    }
}
