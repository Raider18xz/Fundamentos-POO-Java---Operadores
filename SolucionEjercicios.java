public class SolucionEjercicios {
    public double areaTriangulo(double base, double height){
        return (base*height)/2;
    }
    public double SumaNum(double Num1, double Num2){
        return Num1+Num2;
    }
    public double NumCuadrado(double Num1){

        return Num1*Num1;
    }
    public double EurosaDolar(double Num1){
        return Num1*1.08;
    }
    public double AreaCuadrado(double Num1){
        return Num1*Num1;
    }
    public double PerimetroCuadrado(double Num1){
        return Num1+Num1+Num1+Num1;
    }
    public double AreaCilindro(double Num1, double Num2){
        return Num1*Math.PI*Num1*(Num1 + Num2);
    }
    public double VolumenCinlindro(double Num1, double Num2){
        return Math.PI*(Num1*Num1)*Num2;
    }
    public double AreaCirculo(double Num1){
        return 2*Math.PI*Num1;
    }
    public double LongitudCirculo(double Num1){
        return Math.PI*(Num1*Num1);
    }
    public double PromeNum(double Num1,double Num2,double Num3){
        return (Num1*Num2*Num3)/2;
    }

}
